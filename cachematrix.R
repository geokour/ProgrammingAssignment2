## Creating special matrix to cache its inverse

makeCacheMatrix <- function(x = matrix()) {
        m <- NULL
        set <- function(y) {
                x <<- y
                m <<- NULL
        }
        get <- function() x
        setInverse <- function(inverse) j <<- inverse
        getInverse <- function() j
        list(set = set, get = get, setInverse = setInverse, getInverse = getInverse)
}


## Function to compute the inverse of matrix returned by above code, retrieving cached response if one is there

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
        m <- x$getInverse()
        if(!is.null(m)) {
                message("getting cached data")
                return(m)
        }
        matrix <- x$get()
        m <- solve(matrix, ...)
        x$setInverse(m)
        m
}


